public class switch_statement {
    public static void main(String args[]){
         char ch='a';
         switch(ch){
            case 'a':System.out.println("s");
                    break;
            case 'b':System.out.println("b");
                    break;
            case 'c':System.out.println("m");
                    break;
            default :System.out.println("default");
                    break;
         }
    }
}
